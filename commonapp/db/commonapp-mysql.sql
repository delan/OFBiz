
DROP TABLE PARTY;
CREATE TABLE PARTY (
      PARTY_ID VARCHAR(20) NOT NULL,
  PRIMARY KEY (PARTY_ID));

DROP TABLE PARTY_CLASSIFICATION;
CREATE TABLE PARTY_CLASSIFICATION (
      PARTY_ID VARCHAR(20) NOT NULL,
      PARTY_TYPE_ID VARCHAR(20) NOT NULL,
      PARTY_CLASSIFICATION_TYPE_ID VARCHAR(20),
      FROM_DATE DATETIME,
      THRU_DATE DATETIME,
  PRIMARY KEY (PARTY_ID, PARTY_TYPE_ID));

DROP TABLE PARTY_CLASSIFICATION_TYPE;
CREATE TABLE PARTY_CLASSIFICATION_TYPE (
      PARTY_CLASSIFICATION_TYPE_ID VARCHAR(20) NOT NULL,
      PARENT_TYPE_ID VARCHAR(20),
      HAS_TABLE CHAR(1),
      DESCRIPTION VARCHAR(255),
  PRIMARY KEY (PARTY_CLASSIFICATION_TYPE_ID));

DROP TABLE PARTY_TYPE;
CREATE TABLE PARTY_TYPE (
      PARTY_TYPE_ID VARCHAR(20) NOT NULL,
      PARENT_TYPE_ID VARCHAR(20),
      HAS_TABLE CHAR(1),
      DESCRIPTION VARCHAR(255),
  PRIMARY KEY (PARTY_TYPE_ID));

DROP TABLE PARTY_ATTRIBUTE;
CREATE TABLE PARTY_ATTRIBUTE (
      PARTY_ID VARCHAR(20) NOT NULL,
      NAME VARCHAR(60) NOT NULL,
      VALUE VARCHAR(255),
  PRIMARY KEY (PARTY_ID, NAME));

DROP TABLE PARTY_TYPE_ATTR;
CREATE TABLE PARTY_TYPE_ATTR (
      PARTY_TYPE_ID VARCHAR(20) NOT NULL,
      NAME VARCHAR(60) NOT NULL,
  PRIMARY KEY (PARTY_TYPE_ID, NAME));

DROP TABLE USER_LOGIN;
CREATE TABLE USER_LOGIN (
      USER_LOGIN_ID VARCHAR(20) NOT NULL,
      PARTY_ID VARCHAR(20),
      CONTACT_MECHANISM_ID VARCHAR(20),
      CURRENT_USER_ID VARCHAR(20),
      CURRENT_PASSWORD VARCHAR(60),
  PRIMARY KEY (USER_LOGIN_ID));

DROP TABLE LOGIN_ACCOUNT_HISTORY;
CREATE TABLE LOGIN_ACCOUNT_HISTORY (
      USER_LOGIN_ID VARCHAR(20) NOT NULL,
      USER_LOGIN_SEQ_ID VARCHAR(20) NOT NULL,
      FROM_DATE DATETIME,
      THRU_DATE DATETIME,
      PARTY_ID VARCHAR(20),
      USER_ID VARCHAR(20),
      PASSWORD VARCHAR(60),
  PRIMARY KEY (USER_LOGIN_ID, USER_LOGIN_SEQ_ID));

DROP TABLE SECURITY_GROUP;
CREATE TABLE SECURITY_GROUP (
      GROUP_ID VARCHAR(20) NOT NULL,
      DESCRIPTION VARCHAR(255),
  PRIMARY KEY (GROUP_ID));

DROP TABLE SECURITY_PERMISSION;
CREATE TABLE SECURITY_PERMISSION (
      PERMISSION_ID VARCHAR(60) NOT NULL,
      DESCRIPTION VARCHAR(255),
  PRIMARY KEY (PERMISSION_ID));

DROP TABLE SECURITY_GROUP_PERMISSION;
CREATE TABLE SECURITY_GROUP_PERMISSION (
      GROUP_ID VARCHAR(20) NOT NULL,
      PERMISSION_ID VARCHAR(60) NOT NULL,
  PRIMARY KEY (GROUP_ID, PERMISSION_ID));

DROP TABLE USER_LOGIN_SECURITY_GROUP;
CREATE TABLE USER_LOGIN_SECURITY_GROUP (
      USER_LOGIN_ID VARCHAR(20) NOT NULL,
      GROUP_ID VARCHAR(20) NOT NULL,
  PRIMARY KEY (USER_LOGIN_ID, GROUP_ID));

